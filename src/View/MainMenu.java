        private void COActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_COActionPerformed
                conTrans = new ControllerTransaksi();
                conBar = new ControllerProduk();
                conCart = new ControllerKeranjang();

                Object[][] obCart = new Object[TableCart.getRowCount()][TableCart.getColumnCount()];

                int i = 0;
                try {
                        for (i = 0; i < TableCart.getRowCount(); i++) {
                                ProdukModel bm = new ProdukModel();
                                obCart[i][0] = TableCart.getValueAt(i, 0);// id user
                                obCart[i][1] = TableCart.getValueAt(i, 1);// id produk
                                obCart[i][2] = TableCart.getValueAt(i, 2);// nama produk
                                obCart[i][3] = TableCart.getValueAt(i, 3);// jenis
                                obCart[i][4] = TableCart.getValueAt(i, 4);// harga
                                obCart[i][5] = TableCart.getValueAt(i, 5);// jumlah
                                obCart[i][6] = TableCart.getValueAt(i, 6);// total
                                TransaksiModel tm = new TransaksiModel(Integer.parseInt(
                                                obCart[i][0].toString()), mLogin.getID_User(),
                                                Integer.parseInt(obCart[i][5].toString()),
                                                Integer.parseInt(obCart[i][6].toString()));
                                conTrans.saveTrx(tm);
                        }
                        conCart.deleteCart(mLogin);
                        loadCart();
                        loadBarang();
                } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Gagal menambahkan data", "Failed",
                                        JOptionPane.ERROR_MESSAGE);
                }

        }// GEN-LAST:event_COActionPerformed

        private void DeleteCartActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_DeleteCartActionPerformed
                int hasil = 0;
                int quest = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data keranjang?", "Delete Cart",
                                JOptionPane.YES_NO_OPTION);
                if (quest == JOptionPane.YES_OPTION) {
                        conCart = new ControllerKeranjang();
                        hasil = conCart.deleteCart(mLogin);

                        if (hasil == 1) {
                                JOptionPane.showMessageDialog(this, "Cart berhasil dihapus");
                        } else {
                                JOptionPane.showMessageDialog(this, "Data Gagal Dihapus");
                        }
                        loadCart();
                }
        }// GEN-LAST:event_DeleteCartActionPerformed

        private void CartActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_CartActionPerformed
                int hasil = 0;
                conCart = new ControllerKeranjang();
                if (IDPRODUK.equals("") || NAMAPRODUK.equals("") || JUMLAH.equals("") || TIPE.equals("")
                                || TOTAL.equals("")) {
                        JOptionPane.showMessageDialog(this, "Pilih barang terlebih dahulu!!");
                } else if (HARGA.equals("")) {
                        JOptionPane.showMessageDialog(this, "Isi kolom harga terlebih dahulu!!!");
                } else {
                        KeranjangModel cm = new KeranjangModel(mLogin.getID_User(), Integer.parseInt(IDPRODUK.getText()), 
                                        NAMAPRODUK.getText(),Integer.parseInt(JUMLAH.getText()),
                                        Integer.parseInt(HARGA.getText()), TIPE.getText(),
                                        Integer.parseInt(TOTAL.getText()));
                        hasil = conCart.saveCart(cm);
                        if (hasil == 1) {
                                JOptionPane.showMessageDialog(this, "Berhasil menambahkan ke keranjang",
                                                "Berhasil",
                                                JOptionPane.INFORMATION_MESSAGE);
                        } else {
                                JOptionPane.showMessageDialog(this, "Gagal Menambahkan barang ke keranjang", "GAGAL!",
                                                JOptionPane.ERROR_MESSAGE);
                        }
                }
                clearData();
                loadCart();
        }// GEN-LAST:event_CartActionPerformed

